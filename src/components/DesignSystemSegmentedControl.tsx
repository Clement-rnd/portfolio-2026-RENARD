export interface DesignSystemSegmentedControlProps {
  value: DesignSystemView;
  onChange: (value: DesignSystemView) => void;
  className?: string;
}

export function DesignSystemSegmentedControl({
  value,
  onChange,
  className = "",
}: DesignSystemSegmentedControlProps) {
  return (
    <Squircle
      cornerRadius={TRACK_CORNER_RADIUS}
      cornerSmoothing={1}
      fill="#E8E8EA"
      borderWidth={0}
      className={`flex w-full md:inline-flex md:w-auto ${className}`}
    >
      <div className="flex w-full items-center gap-1 p-1 md:w-auto">
        {SEGMENTS.map((segment) => {
          const isActive = value === segment.value;
          return (
            <button
              key={segment.value}
              type="button"
              onClick={() => onChange(segment.value)}
              className={`relative flex min-w-fit flex-1 items-center justify-center gap-1.5 px-3.5 py-1.5 text-sm font-medium leading-none transition-colors md:flex-none ${
                isActive ? "text-heading" : "text-neutral-400"
              }`}
            >
              {isActive && (
                <motion.div
                  layoutId="design-system-segmented-control-bg"
                  className="absolute inset-0"
                  transition={{ type: "spring", duration: 0.25, bounce: 0 }}
                >
                  <Squircle
                    cornerRadius={PILL_CORNER_RADIUS}
                    cornerSmoothing={1}
                    fill="white"
                    borderWidth={0}
                    className="h-full w-full drop-shadow-sm"
                  />
                </motion.div>
              )}
              <span className="relative flex items-center gap-1.5">
                <segment.Icon size={16} />
                {segment.label}
              </span>
            </button>
          );
        })}
      </div>
    </Squircle>
  );
}
